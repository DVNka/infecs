//Задача - написать программу которая вещественные значения меняет на дробные (1,25 -> 125/100)
//Идея1: посчитать знаки после запятой (n) и обозначить 10^n как знаменатель, а числитель записать как строку, просто убрав точку и проверив на минус
#include <stdio.h>
#include <string.h>

int main(){;
    char input[50];     //вводимое пользователем вещественное число
    char numerator[50];     //числитель
    int denominator;        //знаменатель
    int ost = 0;        //счётчик знаков после запятой
    int neg = 0;        //флаг отрицательности вводимого числа 
    int i, j;           //счётчики
    int point = 0;      //флаг прохождения точки

    printf("Введите вещественное число: ");     //ввод вещественного числа в виде строки
    scanf("%s", input);

    for (i = 0; i < strlen(input); i++){        //цикл подсчёта знаков после запятой
        if (input[i] == '.'){
            point = 1;
        } else if (point == 1 && input[i] != '.'){
            ost = ost + 1;
        }
    }
    denominator = 1;
    j = 0;
    for (i = 0; i < ost; i++){
        denominator = denominator * 10;     //создаём знаменатель (просто увеличиваем его в 10 раз за каждый знак после запятой)
    }
    for (i = 0; i < strlen(input); i++){        //цикл для создания числителя
        if (input[i] == '.'){       //пропуск точки
            continue;
        }
        if (input[i] == '-'){   //пропуск минуса и обозначение флага для ответа 
            neg = 1;
            continue;
        }
        numerator[j] = input[i];        //просто переписываем все значения заданного числа без запятой 
        j = j + 1;      //отдельный счётчик для числителя 
    }

    if (neg == 1){                                  //если отрицательно - выводим с минусом, если нет, то нет
        printf("-%s/%d\n", numerator, denominator);
    }else{
    printf("%s/%d\n", numerator, denominator);
    }

    return 0;
}

