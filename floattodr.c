//Идея2: вписывать вводимое пользователем вещественное число в виде double и находить числитель путём сравнения разности изначального числа с её целой частью и погрешности (10^-6)
//В отличие от первого решения, здесь используем более математический подход (думаю, Зайцев это решение лучше оценит, но думайте сами)
//Решения отличаются тем, что в первом решении можно ввести число с ограниченным числом знаков, но точность будет всегда на этом диапазоне, а во втором решении нет ограничения по длине вводимого числа, однако точность при 6+ знаков после запятой будет хромать

#include <stdio.h>

int main(){
    double num;         //вводимое вещественное число   
    int numerator;      //числитель
    int denominator;    //знаменатель
    int ost = 0;        //счётчик знаков после запятой
    int i;              //счётчик
    int neg = 0;        //флаг отрицательности числа

    printf("Введите вещественное число: ");     //ввод
    scanf("%lf", &num);

    if (num < 0){       //смена знака числа при отрицательности для простоты и обозначение данного действие флагом для корректного ответа
        num = -num;
        neg = 1;
    }

    while (ost < 6){                        //цикл создания числителя путём сравнения разности изначального числа и её целой части с погрешностью (т.к. double неточен)
        if (num - (int)num < 0.000001){
            break;
        } else {
            ost++;                  //тут же подводим подсчёт знаков после запятой
            num = num * 10;
        }
    }
    numerator = num;        //переносим полученное значение в числитель

    denominator = 1;
    for (i = 0; i < ost; i++){          //создаём знаменатель
        denominator = denominator * 10;
    }

    if (neg == 1){                  //не забываем про флаг отрицательности и проверяем
        printf("-%d/%d\n", numerator, denominator);
    }
    else {
        printf("%d/%d\n", numerator, denominator);
    }
    return 0;
}

