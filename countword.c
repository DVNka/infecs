//Задача - посчитать количество слов в файле
//Идея - считать строку посимвольно, пока пользователь не закончит или пока не закончится файл

#include <stdio.h>

int main() {
    char ch;           // символ
    int count = 0;     // количество слов
    int in_word = 0;   // флаг, внутри слова или нет

    printf("Введите текст (Ctrl+D для окончания):\n");

    // читаем символы с клавиатуры до конца ввода
    while (1) {
        ch = getchar();    // берём следующий символ из stdin

        if (ch == EOF) {   // если конец ввода (Ctrl+D)
            break;         // выходим из цикла
        }

        // если пробел, перевод строки или табуляция
        if (ch == ' ' || ch == '\n' || ch == '\t') {
            in_word = 0;   // слово закончилось
        }
        // если не пробел и не были в слове
        else if (in_word == 0) {
            in_word = 1;   // начали новое слово
            count = count + 1;  // увеличиваем счётчик
        }
    }

    // выводим результат
    printf("\nСлов в тексте: %d\n", count);

    return 0;
}