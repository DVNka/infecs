//Задача - сравнение многочленов
//Идея - сначала сравнить степени многочленов, тот, у кого степень выше, тот и больше, а если степени равны, то сравнить коэфициенты
//Все действия и сравнения совершать слева направо, т.е. от большей степени к меньшей
//Для хранения многочленов (их коэффициентов) будем использовать массивы размером до 10 (максимум 10 степеней)
//Изначально попросим пользователя ввести размеры многочленов, то есть максимальную степень, которая может быть присутствовать в многочлене
//После размеров просим ввести n+1 коэфициентов для каждого многочлена, где n - его размер
//После этого будем находить реальную степень многочлена (т.е. последний ненулевой коэф.), т.к. коэфициент может равняться нулю

#include <stdio.h>

int main() {
    // Размеры многочленов
    int n1, n2;
    
    // Коэффициенты (максимум 10 степеней)
    int p1[10], p2[10];
    
    // Ввод первого многочлена
    printf("Введите степень первого многочлена: ");
    scanf("%d", &n1);
    
    printf("Введите %d коэффициентов (от x^0 до x^%d):\n", n1 + 1, n1);
    for (int i = 0; i <= n1; i++) {
        scanf("%d", &p1[i]);
    }
    
    // Ввод второго многочлена
    printf("\nВведите степень второго многочлена: ");
    scanf("%d", &n2);
    
    printf("Введите %d коэффициентов (от x^0 до x^%d):\n", n2 + 1, n2);
    for (int i = 0; i <= n2; i++) {
        scanf("%d", &p2[i]);
    }
    
    // Находим реальную степень (последний ненулевой коэффициент)
    int deg1 = n1;
    while (deg1 >= 0 && p1[deg1] == 0) {
        deg1--;
    }
    
    int deg2 = n2;
    while (deg2 >= 0 && p2[deg2] == 0) {
        deg2--;
    }
    
    // Сравниваем
    char result;
    
    if (deg1 > deg2) {
        result = '>';
    }
    else if (deg1 < deg2) {
        result = '<';
    }
    else {
        // Степени равны, сравниваем коэффициенты
        result = '=';
        for (int i = deg1; i >= 0; i--) {
            if (p1[i] > p2[i]) {
                result = '>';
                break;
            }
            else if (p1[i] < p2[i]) {
                result = '<';
                break;
            }
        }
    }
    
    // Вывод результата
    printf("\nРезультат: P1 %c P2\n", result);
    
    return 0;
}

